//! particle type identifiers returned by ARTIO_particle_data::ptype()
enum ptype_bits {
	ARTIO_DM_BIT     = 0,
	ARTIO_STAR_BIT   = 1,
	ARTIO_DEBRIS_BIT = 2
};

//! particle type identifiers, can be combined with logical or
enum ptype { 
	ptype_dm     = 1L << ARTIO_DM_BIT,			//!< selects dark matter particles
	ptype_star   = 1L << ARTIO_STAR_BIT,		//!< selects star particles
	ptype_debris = 1L << ARTIO_DEBRIS_BIT,		//!< selects debris particles
};

//... zero age of a particle is assumed if age is smaller than this number
const double zero_age      = 1.0e-8;


//... content of ARTIO particle data files is fixed, available fields
//... are described in the following two fields

		
//! possible variable names in ARTIO particle files in version 1 and 2 files
const char artio_particle_variables_v12[][64] = {
	{"position_x"},
	{"position_y"},
	{"position_z"},
	{"velocity_x"},
	{"velocity_y"},
	{"velocity_z"},
	{"mass"},
	{"age"},
	{"metallicity"},
	{"particle_ID"},
	{"refinement_level"} };
	
//! possible variable names in ARTIO particle files in version 3 files
