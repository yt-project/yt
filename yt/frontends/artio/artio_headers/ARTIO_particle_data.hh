#define R_CHECK_BIT(var,pos) ((var) & (1<<(pos)))

template< typename Real_, typename IDType_ >
inline bool is_of_type( Real_ age, IDType_ id, int ptype )
{
	//if( ptype==ptype_star )
	if( R_CHECK_BIT( ptype, ARTIO_STAR_BIT ) )
		if( fabs(age)>zero_age && id > 0 )
			return true;
		//else return false;

	//if( ptype==ptype_dm )
	if( R_CHECK_BIT( ptype, ARTIO_DM_BIT ) )
		if( fabs(age)<=zero_age )
			return true;
		//else return false;
			
	//if( ptype==ptype_debris )
	if( R_CHECK_BIT( ptype, ARTIO_DEBRIS_BIT ) )
		if( fabs(age) > zero_age && id == 0 )
			return true;
	return false;
}

#undef R_CHECK_BIT

